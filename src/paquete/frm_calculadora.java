    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        this.segundonumero = Float.parseFloat(lbl_num.getText());
        switch(operador){
            case "+": lbl_num.setText(sincero(primernumero + segundonumero));break;
            case "-": lbl_num.setText(sincero(primernumero - segundonumero));break;
            case "*": lbl_num.setText(sincero(primernumero * segundonumero));break;
            case "/": 
                if(segundonumero == 0){
                    lbl_num.setText("Error . . .");
                }
                else{
                   lbl_num.setText(sincero(primernumero / segundonumero));
                }
            break;           
        }
    }//GEN-LAST:event_jButton17ActionPerformed

